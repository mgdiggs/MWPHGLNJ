<h4>History of the HRAM NJ</h4>
<p><img src="/images/ram.jpg" align="left" style="margin:10px;">The Most Excellent Grand Chapter Holy Royal Arch Masons of the State of Pennsylvania, Prince Hall Affiliated and the Jurisdiction, thereunto belonging, in ample convocation assembled on the tenth day of October, 1854.</p>

<p><img src="/images/rambanner-300x256.gif" align="right" style="margin:10px;" >Know ye that we the officers and members of the said Most Excellent Grand Chapter, Holy Royal Arch Masons by these presets do authorize and Empower our said well-beloved Companions James Harper, Most Excellent Grand High Priest, Emery P. Riley, Excellent Deputy Grand High Priest, James Green, Excellent Grand King, Luke Derrickson, Excellent Grand Scribe, of a Most Excellent Grand Chapter to be know as Most Excellent Prince Hall Grand Chapter, Holy Royal Arch Masons, Jurisdiction of New Jersey, to be located in or near the city of Trenton, New Jersey or within ten miles of the same. And further, we do authorize and Empower our said well-beloved Companions James Harper, Emery P. Riley, James Green and Luke Derrickson, together with the members of the said Most Excellent Prince Hall Grand Chapter of the Jurisdiction of New Jersey to confer the Degrees of Mark Master, Past Master, Most Excellent Master and the Holy Royal Arch on all such candidates as are duly qualified according to the Ancient usages and customs and not otherwise. And further we do authorize and Empower our said well beloved companions and their successors to bear and determine all and singular matters relating to the Order within the Jurisdiction of the said Most Excellent Prince Hall Grand Chapter. And lastly, we do authorize and empower our said well-beloved companions James Harper, Emery F. Riley, James Green, and Luke Derrickson to install their successors after they have been duly elected and chosen and to invest them with all the honors and dignities to their respective offices belong and deliver to them this Warrant Charter.</p>

<p>Such successors from time to time during the continuation of this Most Excellent Grand Chapter. Such installations to be at the Annual Convocation of the Most Excellent Prince Hall Grand Chapter.</p>

<p>Provided always that the laws rules and regulations of said Most Excellent Grand Chapter of Holy Royal Arch Masons are duly obeyed. Otherwise, this charter will be of no force or effect.</p>

<p>Given in open Grand Chapter and signed by our officers this day and date above mentioned at Philadelphia, PA</p>

