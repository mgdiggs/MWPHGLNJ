<h2>History of Prince Hall</h2>
<img src='images/prince_hall.png' style='border: solid;' align='middle'><br>
<p>Prince Hall (1735 – December 4, 1807), was a tireless abolitionist and a leader of the free black community in Boston. Hall tried to gain New England’s enslaved and free blacks a place in some of the most crucial spheres of society, Freemasonry, education and the military. He is considered the founder of “Black Freemasonry” in the United States, known today as Prince Hall Freemasonry. Hall formed the African Grand Lodge of North America. Prince Hall was unanimously elected its Grand Master and served until his death in 1807. He also lobbied tirelessly for education rights for black children and a back-to-Africa movement. Many historians regard Prince Hall as one of the more prominent African American leaders throughout the early national-period of the United States.</p>

