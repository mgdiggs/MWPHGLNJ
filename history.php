<h2>The Origin and Early Progress of Prince Hall Masonry in New Jersey</h2>

<p>Within a two year period, four Lodges, two of which were in obeisance with the First Independent African Grand Lodge of Pennsylvania, and the other two of which were constituents of the Hiram, or rival Grand Lodge of Pennsylvania, were erected in New Jersey. Saint John and Unity Lodges were established in Trenton (July 26, 1845) and Burlington respectively, by the former Grand Lodge. Rising Sun and Mount Moriah Lodges, respectively situated at Camden (May 13, 1847) and Salem (December 27, 1847) were set apart by a latter grand body.</p>
 
<p>Pursuant to a call, representatives from these four Lodge located in New Jersey met in Burlington on June 12, 1848 and “did than and there” erect a Grand Lodge styled. The Most Worshipful Union Grand Lodge of Ancient York Masons for the State of New Jersey and Jurisdiction, Thereunto Belonging”</p>
 
<p>“The Grand Lodge in this state has performed its work for One Hundred Sixty-two years.” That Grand Lodge, the sixth formed among Prince Hall Freemasons became a unit of the National Grand Lodge, allegedly formed in Boston, Massachusetts, June 24, 1847</p>
 
<p>On January 19, 1848 the two Pennsylvania Grand Lodges, having decided to become amenable to the so called National Grand Lodge, settled their differences and effected a union. But that did not last for long for in 1849 another schism occurred in the grand Lodge of the Commonwealth of Pennsylvania, and several Lodges renewing their former allegiances, rallied to the standards of the two formal rival Grand bodies in that State.</p>
 
<p>That unhappy event was almost immediately reflected in the state of New Jersey. Saint John and Unity Lodges, upon notice and for reasons announced withdrew from the Grand Lodge in New Jersey to form another Grand Lodge whose title was identical with that of the parent body. While the Original Grand Lodge remained with the National Compact, the newer grand body declared itself an independent and sovereign body. The second or rival Grand Lodge was established in Camden on April 29, 1850.</p>
 
<p>Thus, a great rivalry fanned by the flame of bitter recrimination and fueled with jealousy and animosity ensued. That state of affairs persisted for a while. But former feelings had been forgiven and forgotten by December 28, 1875. On that day, the Two Grand Lodges, having labored at great lengths to make necessary amends and to complete satisfactory arrangements, adjourned “without day or date” and met in convention on the following day when they did form a unified body, formally titled. The Most Worshipful Grand Lodge, Free and Accepted Masons, for the Sate of New Jersey and Jurisdiction Thereunto Belonging”</p>
 
<p>The consolidation was effected in Schweinhagen’s Hall, Camden.</p>
<p>The spirit of compromise, so notably represented by the events that transpired in that historic convention is of great importance today. Former feelings of bitterness were dissolved; peace and harmony prevailed; and Craftsmen of the present generation know full well that the unseeming chapter in our Local history, which is being candidly reported, has long since been closed. Today, a Grand Lodge sovereign, and founded upon unity and brotherly love, superintends the affairs of Prince Hall Masons in New Jersey.; The Grand Lodge in this state has performed its work for One Hundred Sixty-two years. Our Grand Lodge continues to dispense light and charity, and to supervise the Craft affairs, while fraternal relations with the Prince Hall Family has been maintained continuously.</p>
 
